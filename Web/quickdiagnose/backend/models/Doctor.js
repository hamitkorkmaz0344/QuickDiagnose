const mongoose = require('mongoose');

const doctorSchema = new mongoose.Schema({
  tc: {
    type: String,
    required: true,
    unique: true
  },
  fullName: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true,
    unique: true
  },
  password: {
    type: String,
    required: true
  },
  specialization: {
    type: String,
    required: true
  },
  licenseNumber: {
    type: String,
    required: true,
    unique: true
  }
});

module.exports = mongoose.model('Doctor', doctorSchema);
